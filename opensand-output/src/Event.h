/**
 * @file Event.h
 * @brief The Event class represent an event generated by the application.
 * @author Vincent Duvert <vduvert@toulouse.viveris.com>
 */


#ifndef _EVENT_H
#define _EVENT_H

#include <string>
#include <stdint.h>

/**
 * @brief Event severity levels
 **/
enum event_level_t
{
	LEVEL_DEBUG,   /*!< Debug level */
	LEVEL_INFO,    /*!< Information level */
	LEVEL_WARNING, /*!< Warning level */
	LEVEL_ERROR    /*!< Error level */
};

/**
 * @class Represent an event
 */
class Event
{
	friend class OutputInternal;

protected:
	Event(uint8_t id, const std::string &identifier,
	      event_level_t level);
	virtual ~Event();

private:
	/// the event ID
	uint8_t id;
	/// the event identifier
	std::string identifier;
	/// the event level
	event_level_t level;
};

#endif
